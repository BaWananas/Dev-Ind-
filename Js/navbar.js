


function onMouseOverFonctionnement(){
	document.getElementById("fonctionnement").style.transition="0.5s ease";
	document.getElementById("fonctionnement_txt").style.transition="0.5s ease";
	document.getElementById("fonctionnement_txt").style.color="black";
	document.getElementById("fonctionnement").style.backgroundColor="#FA6900";
	document.getElementById("fonctionnement").style.height="120px";
}

function onMouseOutFonctionnement(){
	document.getElementById("fonctionnement").style.transition="0.5s ease";
	document.getElementById("fonctionnement_txt").style.transition="0.5s ease";
	document.getElementById("fonctionnement_txt").style.color="white";
	document.getElementById("fonctionnement").style.backgroundColor="#3a3737";
	document.getElementById("fonctionnement").style.height="70px";
}



